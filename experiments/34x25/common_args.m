    dbstop if error
    dbstop if warning
  else  % OCTAVE
    debug_on_error = 1;
    pkg load image;
    pkg load statistics;
  end;

  % Add absolute path to the 'code' directory
  script_dir = fileparts(which(mfilename));
  code_dir = fullfile(script_dir, '..', '..', 'code');
  addpath(genpath(code_dir));

  opts = {'small'};

  cargs = {  'parallel', false, 'debug', 1:10, 'ac.debug', 1:10, 'p.debug', 1:10, ...
             'ac.randState', 2,   'p.randState', 2, ...
             'distn',   {'normem2'},     'mu',        0, 'sigma', [2, 15], ...
             ... %  'spacing', struct('mean', 2.318, 'maxdiffpct', 0.5), ...%2245), ...
             'nHidden', 850*4,          'hpl',       4,    'nConns',      15, ...
             'deType', 'de', ...
             ...% Input
             'ac.zscore', 0.25, ...
             'ac.tol',    0*34/25, ... %tolerance for disconnected pixels
             ... % Training
             'ac.errorType', 2, ...
             'ac.XferFn',   [6 1], ...
             'ac.useBias',  1, ...
             ...
             'ac.WeightInitScale', 0.01, ...
             'ac.WeightInitType', 'sprandn', ...
             'ac.wlim',            inf * [-1 1], ...
             'ac.dropout', 0.0, ...
             'ac.noise_input',     0.00, ...
             'ac.lambda',          0.00,         ...% regularization
             'ac.Pow', 1, ...
             'p.useBias',  1, ...
             ...
             'p.WeightInitScale', 0.10, ...
             'p.WeightInitType', 'sprandn', ...
             'p.wlim',            [-inf inf],         ...% regularization
             'p.dropout', 0.0, ...
             'p.noise_input',     0,         ...% regularization
             'p.lambda',          0.00,         ...% regularization
             'p.Pow', 1, ...
             'ac.rej.props', {'err'},                'p.rej.props', {'err'}, ... %err,max,nan
             'ac.rej.type',  {'max'},                'p.rej.type',  {'sample_std'}, ...
             'ac.rej.width', [NaN],                  'p.rej.width', [3], ...%3] ...
             ... %output
             'out.data', {'info','mat'}, ...
             'out.plots', {'png'},  ...
             'out.titles', {'LH (wide)', 'RH (narrow)'},  ...
             varargin{:}
           };
