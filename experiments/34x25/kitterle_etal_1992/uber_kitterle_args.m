function [args,opts] = uber_kitterle_args(varargin)
%

  % Get shared args
  script_dir = fileparts(which(mfilename));
  addpath(fullfile(script_dir, '..'));

  stats = {};
  plots = {stats{:}};

  [args,opts] = uber_args( ...
    'runs', 2, ...
    'stats', stats, 'plots', plots, ...
    ...
    'p.zscore', 0.15, ...
    'p.XferFn', [8 3], ...  % logistic; binary classification
    'p.EtaInit', 2E-3, ...
    'p.Acc', 1E-6, 'p.Dec', 0.025, ...
    'p.nHidden', 101, ...
    'p.MaxIterations', 200, ...
    'p.wlim', 2*[-1 1], varargin{:} ...
  );

opts = {opts{:}, 'cycles', [3 5], 'nphases', 1, 'nthetas', 16};
