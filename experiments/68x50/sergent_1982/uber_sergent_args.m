function [args,opts] = uber_sergent_args(varargin)
%

  % Get shared args
  script_dir = fileparts(which(mfilename));
  addpath(fullfile(script_dir, '..'));

  stats = {};
  plots = {'ls-bars', stats{:}};

  [args,opts] = uber_args( ... %Network structure
    'runs', 50, ...
    'stats', stats, 'plots', plots, ...
    ...
    'p.XferFn', [6 3], ...  %sigmoid->sigmoid
    'p.zscore', 0.15, ...
    'p.EtaInit', 2E-3, ...
    'p.Acc', 1E-7, 'p.Dec', 0.25, ...
    'p.nHidden', 10, ...
    'p.dropout', 0.5, ...
    'p.wlim', 0.5*[-1 1], ...
    'p.lambda', 0.01, ...
    'p.MaxIterations', 250, ...
    varargin{:} ...
  );
